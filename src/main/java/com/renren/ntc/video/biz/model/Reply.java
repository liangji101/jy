public class Reply {

    private int id;
    private int uid;
    private int source;
    private int replyId;
    private String content;
    private Date ctime;
    private String ftime;   //格式化好的时间
    private byte candel;

    private String username;
    private String headUrl;

    private Feed feed;

    public Feed getFeed() {
        return feed;
    }

    public void setFeed(Feed feed) {
        this.feed = feed;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

    public int getSource() {
        return source;
    }

    public void setSource(int source) {
        this.source = source;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public Date getCtime() {
        return ctime;
    }

    public void setCtime(Date ctime) {
        this.ctime = ctime;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getReplyId() {
        return replyId;
    }

    public void setReplyId(int replyId) {
        this.replyId = replyId;
    }

    public String getFtime() {
        return ftime;
    }

    public void setFtime(String ftime) {
        this.ftime = ftime;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getHeadUrl() {
        return headUrl;
    }

    public void setHeadUrl(String headUrl) {
        this.headUrl = headUrl;
    }

    public byte getCandel() {
        return candel;
    }

    public void setCandel(byte candel) {
        this.candel = candel;
    }

    @Override
    public String toString() {
        final StringBuilder sb = new StringBuilder();
        sb.append("Reply");
        sb.append("{candel=").append(candel);
        sb.append(", id=").append(id);
        sb.append(", uid=").append(uid);
        sb.append(", source=").append(source);
        sb.append(", replyId=").append(replyId);
        sb.append(", content='").append(content).append('\'');
        sb.append(", ctime=").append(ctime);
        sb.append(", ftime='").append(ftime).append('\'');
        sb.append(", username='").append(username).append('\'');
        sb.append(", headUrl='").append(headUrl).append('\'');
        sb.append(", feed=").append(feed);
        sb.append('}');
        return sb.toString();
    }
}
