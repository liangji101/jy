package com.renren.ntc.video.biz.model;


import java.io.Serializable;

/**
 * 持久化的session_key和session_secret
 * 
 * @author haobo.cui@opi-corp.com 2009-3-16 下午12:12:23
 */

public class InfiniteSk implements Serializable  {
	private int appId;// session_key 密钥
	private int userId;// session_secret 密钥
	private String sessionKey;// sessionKey
	private String sessionSecret;// sessionSecret

	public String getSessionKey() {
		return sessionKey;
	}

	public void setSessionKey(String sessionKey) {
		this.sessionKey = sessionKey;
	}

	public String getSessionSecret() {
		return sessionSecret;
	}

	public void setSessionSecret(String sessionSecret) {
		this.sessionSecret = sessionSecret;
	}

	public int getAppId() {
		return appId;
	}

	public void setAppId(int appId) {
		this.appId = appId;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

}
