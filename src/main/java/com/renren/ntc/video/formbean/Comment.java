package com.renren.ntc.video.formbean;

public class Comment {
	private int ownner;
	private String ownnerName;
	private String ownnerHeadurl;
	private String content;
	private String createTime;
	private String commentId;

    public String getCommentId() {
        return commentId;
    }

    public void setCommentId(String commentId) {
        this.commentId = commentId;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public int getOwnner() {
        return ownner;
    }

    public void setOwnner(int ownner) {
        this.ownner = ownner;
    }

    public String getOwnnerHeadurl() {
        return ownnerHeadurl;
    }

    public void setOwnnerHeadurl(String ownnerHeadurl) {
        this.ownnerHeadurl = ownnerHeadurl;
    }

    public String getOwnnerName() {
        return ownnerName;
    }

    public void setOwnnerName(String ownnerName) {
        this.ownnerName = ownnerName;
    }
}
